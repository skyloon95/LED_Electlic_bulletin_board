{
	char change; // 아스키코드에서 변수에맞는 번지수로 교환한 후 저장될 변수
	
	if(Division(Character)==0)
		change=Character-65;

	else if(Division(Character)==1)
		change=Character-97;
		
	else if(Division(Character)==2)
		change=Character-48;

	else if(Division(Character)==3){
		switch (Character){
		case '!':
			change=0;
			break;

		case '?':
			change=1;
			break;

		case '(':
			change=2;
			break;

		case ')':
			change=3;
			break;

		case '-':
			change=4;
			break;

		case '+':
			change=5;
			break;

		case '*':
			change=6;
			break;

		case '/':
			change=7;
			break;

		case '%':
			change=8;
			break;

		case '~':
			change=9;
			break;
		case 32:
			change=10;
			break;
		}
	}
return change;
}

//행렬을 이동하며 몇번쨰 x축에서 처음 1이 등장하는지 찾아내는 함수.
char Find_Start (char Character)
{
	char division; // 인식할 문자의 종류를 저장해놓을 변수
	char character; // 인식할 문자를 저장해놓을 변수
	char checkx; // x축
	char checky; // y축
	char save_start=0; // 시작점 저장

	if(Character==32)
		return 0;

	division = Division(Character);

	character = Aski_Change(Character);

	for(checkx=0 ; checkx<10 ; checkx++, save_start++)
		for(checky=0 ; checky<10 ;checky++)
			if(Characteristic[division][character][checky][checkx]==1)
				return save_start;
}

//행렬을 이동하며 몇번째 x축에서 마지막 1이 등장하는지 찾아내는 함수.
char Find_End (char Character)
{
	char division; // 인식할 문자의 종류를 저장해놓을 변수
	char character; // 인식할 문자를 저장해놓을 변수
	char checkx; // x축
	char checky; // y축
	char save_end=9; // 종료점 저장

	if(Character==32)
		return 3;

	division = Division(Character);

	character = Aski_Change(Character);
	
	for(checkx=9 ; checkx>=0 ; checkx--, save_end--)
		for(checky=0 ; checky<10 ;checky++)
			if(Characteristic[division][character][checky][checkx]==1)
				return save_end;
}

//폰트의 폭을 구하는 함수
char Long_Character (char Character)
{
	char Long; // 폰트의 폭

	Long = 1+(Find_End(Character)-Find_Start(Character)); // 끝나는부분에서 시작하는부분을 뻄.

	return Long;
}

//문자열의 길이를 구하는 함수
int Long_String (char *String)
{
	int Long=0; // 문자열의 길이

	for(Long=0 ; Long<300 ; Long++)
		if(String[Long]==0)
			return Long;
}

// 버퍼를 만드는 함수
void Buffering (char *String, char **Buffer)
{
	char division; // 인식할 문자의 종류를 저장해놓을 변수
	char character; // 인식할 문자를 저장해놓을 변수 
	int bufferx=0; //버퍼의 x축 
	char buffery=0; //버퍼의 y축

	int point_bufferx=0; // 현재 x축 커서의 위치

	int Switch; // 배열속에서 커서 위치를 이동시키는 변수

	for(Switch=0 ; Switch<Long_String(String) ; Switch++){ // 문자열에서 각문자 하나하나를 인식하고 다음문자로 넘기는 반복문.
		if(point_bufferx+Long_Character(String[Switch])>1999)
			break;

		division = Division(String[Switch]);
		
		character = Aski_Change(String[Switch]);

		for(buffery=0 ; buffery<10 ; buffery++) // 버퍼의 y축을 이동시키는 반복문
			for(bufferx=0 ; bufferx < Long_Character(String[Switch]) ; bufferx++) // 버퍼의 x축을 이동시키는 반복문
				Buffer[buffery][point_bufferx + bufferx] = Characteristic[division][character][buffery][bufferx + Find_Start(String[Switch])]; // 버퍼에 폰트를 씀
		point_bufferx+=Long_Character(String[Switch])+2;
	}
}

